CREATE FUNCTION TotalBiayaKunjungan (idK INT) RETURNS FLOAT DETERMINISTIC
BEGIN
    DECLARE total FLOAT;
    SELECT SUM(o.harga * r.jumlah)
    INTO total
    FROM resep r
    JOIN obat o ON r.id_obat = o.id_obat
    WHERE r.id_kunjungan = idK;
    RETURN IFNULL(total, 0);
END$$

